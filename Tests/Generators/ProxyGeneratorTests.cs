    // ── Interceptor chaining ─────────────────────────────────────────────────────

    [Test]
    public void Generator_GeneratesInterceptorChainLoop()
    {
        var source = InterceptorBoilerplate + """
            [InterceptedBy(typeof(MyInterceptor))]
            public class MyService
            {
                public virtual void Execute() { }
            }
            """;

        var generated = RunGenerator(source)[0].SourceText.ToString();
        using (Assert.EnterMultipleScope())
        {
            Assert.That(generated, Does.Contain("Func<IInvocation, Task<object?>> next = (inv) => inv.ProceedAsync();"));
            Assert.That(generated, Does.Contain("for (int i = _interceptors.Length - 1; i >= 0; i--)"));
            Assert.That(generated, Does.Contain("var interceptor = _interceptors[i];"));
            Assert.That(generated, Does.Contain("var currentNext = next;"));
            Assert.That(generated, Does.Contain("next = (inv) => interceptor.InterceptAsync(new Invocation"));
            Assert.That(generated, Does.Contain("ProceedAsync = () => currentNext(inv)"));
        }
    }

