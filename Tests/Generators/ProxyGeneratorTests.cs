    // Shared interceptor boilerplate injected into every test source string
    private const string InterceptorBoilerplate = """
        using CoreOne;
        using CoreOne.Attributes;
        using System.Threading.Tasks;

        public class MyInterceptor : IAsyncInterceptor
        {
            public Task<object?> InterceptAsync(IInvocation invocation) => invocation.ProceedAsync();
        }

        """;

    private static Compilation CreateCompilation(string source)
    {
        var loadedAssemblies = AppDomain.CurrentDomain.GetAssemblies()
            .Where(a => !a.IsDynamic && !string.IsNullOrEmpty(a.Location))
            .Select(a => MetadataReference.CreateFromFile(a.Location))
            .Cast<MetadataReference>();

        // Ensure the CoreOne assembly is in the references (needed for InterceptedByAttribute, IAsyncInterceptor etc.)
        var coreOneRef = MetadataReference.CreateFromFile(typeof(InterceptedByAttribute).Assembly.Location);

        var references = loadedAssemblies.Append(coreOneRef).ToArray();

        return CSharpCompilation.Create(
            "TestAssembly",
            [CSharpSyntaxTree.ParseText(source)],
            references,
            new CSharpCompilationOptions(OutputKind.DynamicallyLinkedLibrary));
    }

    private static ImmutableArray<GeneratedSourceResult> RunGenerator(string source)
    {
        var compilation = CreateCompilation(source);
        var generator = new ProxyGenerator();
        var driver = CSharpGeneratorDriver.Create(generator);
        driver = (CSharpGeneratorDriver)driver.RunGenerators(compilation);
        return driver.GetRunResult().Results[0].GeneratedSources;
    }

