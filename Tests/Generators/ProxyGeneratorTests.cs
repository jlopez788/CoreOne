    // ── Static MethodInfo caching ────────────────────────────────────────────────

    [Test]
    public void Generator_GeneratesStaticMethodInfoCaching()
    {
        var source = InterceptorBoilerplate + """
            [InterceptedBy(typeof(MyInterceptor))]
            public class MyService
            {
                public virtual void Execute() { }
            }
            """;

        // Safe method name for Execute() with no params is "Execute_"
        var generated = RunGenerator(source)[0].SourceText.ToString();
        using (Assert.EnterMultipleScope())
        {
            Assert.That(generated, Does.Contain("private static readonly MethodInfo _Execute_Method"));
            Assert.That(generated, Does.Contain("private static MethodInfo GetMethodInfo_Execute_()"));
            Assert.That(generated, Does.Contain("typeof(MyService).GetMethod(\"Execute\""));
            Assert.That(generated, Does.Contain("BindingFlags.Public | BindingFlags.Instance"));
            // Method field is referenced inside the invocation
            Assert.That(generated, Does.Contain("Method = _Execute_Method,"));
        }
    }

