    // ── Generic attribute form (C# 11+) ─────────────────────────────────────────

    [Test]
    public void Generator_WithGenericAttributeForm_GeneratesProxy()
    {
        // InterceptedByAttribute<T> is itself an attribute (generic attribute, C# 11+).
        // The syntax predicate checks baseFullName == "CoreOne.Attributes.InterceptedByAttribute"
        // which matches for InterceptedByAttribute<T> whose base IS InterceptedByAttribute.
        const string source = """
            using CoreOne;
            using CoreOne.Attributes;
            using System.Threading.Tasks;

            public class MyInterceptor : IAsyncInterceptor
            {
                public Task<object?> InterceptAsync(IInvocation invocation) => invocation.ProceedAsync();
            }

            [InterceptedBy<MyInterceptor>]
            public class MyService
            {
                public virtual void Execute() { }
            }
            """;

        var compilation = CreateCompilation(source);
        var generator = new ProxyGenerator();
        var driver = CSharpGeneratorDriver.Create(generator);
        driver = (CSharpGeneratorDriver)driver.RunGenerators(compilation);
        var sources = driver.GetRunResult().Results[0].GeneratedSources;

        Assert.That(sources, Has.Length.EqualTo(1));
        var generated = sources[0].SourceText.ToString();
        using (Assert.EnterMultipleScope())
        {
            Assert.That(sources[0].HintName, Is.EqualTo("MyServiceProxy.g.cs"));
            Assert.That(generated, Does.Contain("public partial class MyServiceProxy : MyService"));
            Assert.That(generated, Does.Contain("MyInterceptor interceptor0"));
        }
    }
