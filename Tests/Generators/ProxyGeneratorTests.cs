    // ── Method override variants ─────────────────────────────────────────────────

    [Test]
    public void Generator_WithVoidVirtualMethod_GeneratesOverrideWithSyncChaining()
    {
        var source = InterceptorBoilerplate + """
            [InterceptedBy(typeof(MyInterceptor))]
            public class MyService
            {
                public virtual void Execute() { }
            }
            """;

        var generated = RunGenerator(source)[0].SourceText.ToString();
        using (Assert.EnterMultipleScope())
        {
            Assert.That(generated, Does.Contain("public override void Execute()"));
            Assert.That(generated, Does.Contain("base.Execute();"));
            // void ProceedAsync returns null
            Assert.That(generated, Does.Contain("return null;"));
            // sync chaining via GetAwaiter().GetResult()
            Assert.That(generated, Does.Contain(".GetAwaiter().GetResult()"));
            // void methods must NOT emit a return-cast statement
            Assert.That(generated, Does.Not.Contain("return (void)result"));
        }
    }

    [Test]
    public void Generator_WithSyncReturnVirtualMethod_GeneratesOverrideWithCastReturn()
    {
        var source = InterceptorBoilerplate + """
            [InterceptedBy(typeof(MyInterceptor))]
            public class MyService
            {
                public virtual int Calculate(int x, int y) => x + y;
            }
            """;

        var generated = RunGenerator(source)[0].SourceText.ToString();
        using (Assert.EnterMultipleScope())
        {
            Assert.That(generated, Does.Contain("public override int Calculate(int x, int y)"));
            Assert.That(generated, Does.Contain("return base.Calculate(x, y)"));
            Assert.That(generated, Does.Contain(".GetAwaiter().GetResult()"));
            Assert.That(generated, Does.Contain("return (int)result!;"));
        }
    }

    [Test]
    public void Generator_WithAsyncTaskVirtualMethod_GeneratesAsyncOverrideWithoutReturn()
    {
        var source = InterceptorBoilerplate + """
            [InterceptedBy(typeof(MyInterceptor))]
            public class MyService
            {
                public virtual async System.Threading.Tasks.Task ExecuteAsync()
                {
                    await System.Threading.Tasks.Task.CompletedTask;
                }
            }
            """;

        var generated = RunGenerator(source)[0].SourceText.ToString();
        using (Assert.EnterMultipleScope())
        {
            Assert.That(generated, Does.Contain("public override async System.Threading.Tasks.Task ExecuteAsync()"));
            // ProceedAsync awaits the base call and returns null for non-generic Task
            Assert.That(generated, Does.Contain("await base.ExecuteAsync();"));
            Assert.That(generated, Does.Contain("return null;"));
            // async chaining via await (not .GetAwaiter().GetResult())
            Assert.That(generated, Does.Contain("var result = await next(invocation);"));
            Assert.That(generated, Does.Not.Contain(".GetAwaiter().GetResult()"));
            // no cast-return for plain Task
            Assert.That(generated, Does.Not.Contain("return (System.Threading.Tasks.Task)result"));
        }
    }

